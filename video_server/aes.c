/**
 * @brief AES-128-ECB 加密 16 字节
 * @param key    16字节密钥（从AT24C64 0x0020读取）
 * @param in     输入16字节明文
 * @param out    输出16字节密文
 */
void aes128_ecb_encrypt(const uint8_t *key, const uint8_t *in, uint8_t *out)
{
    uint8_t roundKey[176]; // 扩展后的轮密钥
    uint8_t state[16];     // AES状态矩阵

    // 1. 初始化状态
    memcpy(state, in, 16);
    // 2. 密钥扩展
    KeyExpansion(key, roundKey);
    // 3. 初始轮密钥加
    AddRoundKey(state, roundKey);

    // 4. 9轮标准变换
    for (int round = 1; round < 10; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, roundKey + round*16);
    }

    // 5. 最后一轮（无列混合）
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, roundKey + 10*16);

    // 输出结果
    memcpy(out, state, 16);
}

/**
 * @brief AES-128-ECB 解密 16 字节
 * @param key    16字节密钥（与加密相同）
 * @param in     输入16字节密文
 * @param out    输出16字节明文
 */
void aes128_ecb_decrypt(const uint8_t *key, const uint8_t *in, uint8_t *out)
{
    uint8_t roundKey[176];
    uint8_t state[16];

    // 1. 初始化
    memcpy(state, in, 16);
    KeyExpansion(key, roundKey);
    AddRoundKey(state, roundKey + 10*16);

    // 2. 逆变换9轮
    for (int round = 9; round >= 1; round--) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, roundKey + round*16);
        InvMixColumns(state);
    }

    // 3. 最后逆变换
    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, roundKey);

    // 输出明文
    memcpy(out, state, 16);
}
