/**
 * @brief PKCS#7 填充
 * @param data      输入数据缓冲区（必须预留足够空间）
 * @param length    原始数据长度
 * @return          填充后长度
 */
uint32_t pkcs7_pad(uint8_t *data, uint32_t length)
{
    uint8_t padding = AES_BLOCK_SIZE - (length % AES_BLOCK_SIZE);

    for (uint8_t i = 0; i < padding; i++) {
        data[length + i] = padding;
    }

    return length + padding;
}

/**
 * @brief PKCS#7 去填充
 * @param data      解密后的数据
 * @param length    当前数据长度
 * @return          去填充后的真实长度
 */
uint32_t pkcs7_unpad(uint8_t *data, uint32_t length)
{
    if (length == 0) return 0;

    uint8_t padding = data[length - 1];

    if (padding > AES_BLOCK_SIZE) return length; // 简单安全检查

    return length - padding;
}
// ==================== AES 轮常数 ====================
static const uint8_t Rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
};

/**
 * @brief 字节代换（加密用）
 * @param state AES 16字节状态矩阵
 */
static void SubBytes(uint8_t *state)
{
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[state[i]];
    }
}

/**
 * @brief 逆字节代换（解密用）
 * @param state AES 16字节状态矩阵
 */
static void InvSubBytes(uint8_t *state)
{
    for (int i = 0; i < 16; i++) {
        state[i] = rsbox[state[i]];
    }
}

/**
 * @brief 行移位（加密用）
 * @param s AES 16字节状态矩阵
 */
static void ShiftRows(uint8_t *s)
{
    uint8_t temp;
    // 第1行循环左移1位
    temp = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = temp;
    // 第2行循环左移2位
    temp = s[2]; s[2] = s[10]; s[10] = temp;
    temp = s[6]; s[6] = s[14]; s[14] = temp;
    // 第3行循环左移3位
    temp = s[3]; s[3] = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = temp;
}

/**
 * @brief 逆行移位（解密用）
 * @param s AES 16字节状态矩阵
 */
static void InvShiftRows(uint8_t *s)
{
    uint8_t temp;
    // 第1行循环右移1位
    temp = s[13]; s[13] = s[9]; s[9] = s[5]; s[5] = s[1]; s[1] = temp;
    // 第2行循环右移2位
    temp = s[10]; s[10] = s[2]; s[2] = temp;
    temp = s[14]; s[14] = s[6]; s[6] = temp;
    // 第3行循环右移3位
    temp = s[7];  s[7] = s[11]; s[11] = s[15]; s[15] = s[3]; s[3] = temp;
}

/**
 * @brief 字节乘法辅助函数（GF(2^8)）
 */
static uint8_t xtime(uint8_t x)
{
    return (x << 1) ^ (((x >> 7) & 1) * 0x1b);
}

/**
 * @brief GF(2^8) 有限域乘法
 */
static uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    uint8_t hi_bit_set;
    for (int i = 0; i < 8; i++) {
        if (b & 1) {
            p ^= a;
        }
        hi_bit_set = (a & 0x80);
        a <<= 1;
        if (hi_bit_set) {
            a ^= 0x1b;
        }
        b >>= 1;
    }
    return p;
}

/**
 * @brief 列混合（加密用）
 * @param s AES 16字节状态矩阵
 */
static void MixColumns(uint8_t *s) {
    for (int i = 0; i < 4; i++) {
        uint8_t a = s[i+0];
        uint8_t b = s[i+4];
        uint8_t c = s[i+8];
        uint8_t d = s[i+12];
        uint8_t ta = a ^ b ^ c ^ d;
        uint8_t tb = xtime(a ^ b);
        uint8_t tc = xtime(b ^ c);
        uint8_t td = xtime(c ^ d);
        uint8_t te = xtime(d ^ a);
        s[i+0] ^= tb ^ ta;
        s[i+4] ^= tc ^ ta;
        s[i+8] ^= td ^ ta;
        s[i+12] ^= te ^ ta;
    }
}

/**
 * @brief 逆列混合（解密用，修正版）
 * @param s AES 16字节状态矩阵
 */
static void InvMixColumns(uint8_t *s) {
    uint8_t a[4];
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            a[j] = s[i + 4*j];
        }
        s[i + 4*0] = gmul(a[0], 0x0E) ^ gmul(a[1], 0x0B) ^ gmul(a[2], 0x0D) ^ gmul(a[3], 0x09);
        s[i + 4*1] = gmul(a[0], 0x09) ^ gmul(a[1], 0x0E) ^ gmul(a[2], 0x0B) ^ gmul(a[3], 0x0D);
        s[i + 4*2] = gmul(a[0], 0x0D) ^ gmul(a[1], 0x09) ^ gmul(a[2], 0x0E) ^ gmul(a[3], 0x0B);
        s[i + 4*3] = gmul(a[0], 0x0B) ^ gmul(a[1], 0x0D) ^ gmul(a[2], 0x09) ^ gmul(a[3], 0x0E);
    }
}

/**
 * @brief 轮密钥加
 * @param state AES 16字节状态矩阵
 * @param key   当前轮密钥
 */
static void AddRoundKey(uint8_t *state, const uint8_t *key)
{
    for (int i = 0; i < 16; i++) {
        state[i] ^= key[i];
    }
}

/**
 * @brief 密钥扩展：将16字节密钥扩展为176字节轮密钥
 * @param key  输入16字节密钥
 * @param w    输出扩展密钥
 */
static void KeyExpansion(const uint8_t *key, uint8_t *w)
{
    // 先拷贝原始密钥
    for (int i = 0; i < 16; i++) {
        w[i] = key[i];
    }

    // 扩展剩余轮密钥
    for (int i = 4; i < 44; i++) {
        uint8_t temp[4];
        for (int j = 0; j < 4; j++) {
            temp[j] = w[(i-1)*4 + j];
        }

        // 每4组做一次循环移位、S盒、轮常数
        if (i % 4 == 0) {
            uint8_t t = temp[0];
            temp[0] = temp[1]; temp[1] = temp[2]; temp[2] = temp[3]; temp[3] = t;
            for (int j = 0; j < 4; j++) {
                temp[j] = sbox[temp[j]];
            }
            temp[0] ^= Rcon[i/4];
        }

        for (int j = 0; j < 4; j++) {
            w[i*4 + j] = w[(i-4)*4 + j] ^ temp[j];
        }
    }
}

